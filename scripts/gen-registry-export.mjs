#!/usr/bin/env node
/**
 * scripts/gen-registry-export.mjs
 *
 * Derives the community-facing data files from the single source of truth
 * (lib/gen/backends.ts). Never hand-edit the CSV/JSON — edit the registry and
 * re-run this. The /studio/engines page, the blog guide, and the public OSS
 * mirror all read these derived files.
 *
 *   node --experimental-strip-types scripts/gen-registry-export.mjs
 *
 * Outputs:
 *   data/gen/engine-guide.json   — full structured registry (for the mirror + tooling)
 *   data/gen/engine-guide.csv    — flat decision table (best models / which sub / when paid)
 */

import { writeFileSync, mkdirSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath, pathToFileURL } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')
const OUT_DIR = resolve(ROOT, 'data/gen')

const { genBackends, GEN_VERSION, GEN_UPDATED } = await import(
  pathToFileURL(resolve(ROOT, 'lib/gen/backends.ts')).href
)

function csvCell(v) {
  const s = Array.isArray(v) ? v.join(' | ') : v == null ? '' : String(v)
  return /[",\n]/.test(s) ? `"${s.replace(/"/g, '""')}"` : s
}

const COLUMNS = [
  ['id', (b) => b.id],
  ['name', (b) => b.name],
  ['modality', (b) => b.kind],
  ['tier', (b) => b.tier],
  ['status', (b) => b.status],
  ['native_to_harness', (b) => b.nativeToHarness ?? []],
  ['subscription', (b) => b.subscription ?? ''],
  ['cost_shape', (b) => b.cost.shape],
  ['cost_summary', (b) => b.cost.summary],
  ['extra_api_cost', (b) => (b.cost.shape === 'native-included' ? 'no' : 'yes')],
  ['models', (b) => b.models],
  ['best_for', (b) => b.bestFor],
  ['not_when', (b) => b.notWhen],
  ['when_worth_paid', (b) => b.whenWorthPaid ?? ''],
  ['defers_to', (b) => b.defersTo ?? ''],
  ['docs_url', (b) => b.docsUrl ?? ''],
]

mkdirSync(OUT_DIR, { recursive: true })

const header = COLUMNS.map(([h]) => h).join(',')
const rows = genBackends.map((b) => COLUMNS.map(([, fn]) => csvCell(fn(b))).join(','))
const csv = [header, ...rows].join('\n') + '\n'
writeFileSync(resolve(OUT_DIR, 'engine-guide.csv'), csv)

const json = {
  version: GEN_VERSION,
  updated: GEN_UPDATED,
  source: 'lib/gen/backends.ts',
  note: 'Generated by scripts/gen-registry-export.mjs — do not hand-edit.',
  engines: genBackends,
}
writeFileSync(resolve(OUT_DIR, 'engine-guide.json'), JSON.stringify(json, null, 2) + '\n')

const native = genBackends.filter((b) => b.cost.shape === 'native-included').length
console.log(
  `gen-registry-export: ${genBackends.length} engines (${native} native/no-extra-cost) → data/gen/engine-guide.{csv,json}`
)
