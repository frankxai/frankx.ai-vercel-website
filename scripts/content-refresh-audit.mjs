#!/usr/bin/env node
// Content refresh-priority audit.
//
// The question this answers: "Of all our posts, which are BOTH high-traffic AND
// factually decaying — so which do we re-fact-check first?"
//
// It joins two signals:
//   1. Staleness risk  — derived from each post's frontmatter date + how much
//      version-pinned / dated-comparison language it contains (these decay fastest).
//   2. Traffic         — from docs/ops/analytics/traffic-latest.json (the weekly paste).
//
// priority = visitors * stalenessScore  → a high-traffic stale comparison post
// floats to the top; an evergreen technique post or a zero-traffic post sinks.
//
// Usage:
//   node scripts/content-refresh-audit.mjs            # print ranked table
//   node scripts/content-refresh-audit.mjs --write    # also write REFRESH-QUEUE.md
//   node scripts/content-refresh-audit.mjs --all      # include posts with no traffic

import { readFileSync, writeFileSync, readdirSync, existsSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const BLOG_DIR = join(ROOT, 'content', 'blog')
const TRAFFIC = join(ROOT, 'docs', 'ops', 'analytics', 'traffic-latest.json')
const OUT = join(ROOT, 'docs', 'ops', 'analytics', 'REFRESH-QUEUE.md')

const args = process.argv.slice(2)
const doWrite = args.includes('--write')
const includeAll = args.includes('--all')

// Signals that a claim is pinned to a fast-moving fact (model versions, dated comparisons).
const VERSION_RX =
  /\b(gen-?\d(?:\.\d)?|veo ?\d|sora ?\d|gpt-?[45]\b|claude (?:opus |sonnet |haiku )?\d|gemini ?\d|kling ?\d|pika ?\d(?:\.\d)?|llama ?\d|deepseek[- ]?(?:r)?\d|grok ?\d|seedance ?\d|v\d+\.\d+)\b/gi
const COMPARISON_RX = /\b(vs\.?|versus|compared|which (?:model|tool|one)|best .* (?:for|in 20\d\d)|ranked|landscape|roundup)\b/i

const NOW = Date.parse('2026-06-05') // pinned: Date.now() is unavailable in some harnesses; bump when re-running

function frontmatter(raw) {
  const m = raw.match(/^---\n([\s\S]*?)\n---/)
  if (!m) return { fm: {}, body: raw }
  const fm = {}
  for (const line of m[1].split('\n')) {
    const kv = line.match(/^(\w+):\s*"?([^"]*)"?\s*$/)
    if (kv) fm[kv[1]] = kv[2].trim()
  }
  return { fm, body: raw.slice(m[0].length) }
}

function staleness(fm, body, slug) {
  const title = fm.title || slug
  const ageDays = fm.date ? Math.max(0, (NOW - Date.parse(fm.date)) / 86400000) : 365
  const months = ageDays / 30
  const versionHits = (title + '\n' + body).match(VERSION_RX)?.length || 0
  const datedTitle = /\b20\d\d\b/.test(title) || /\b20\d\d\b/.test(slug)
  const comparison = COMPARISON_RX.test(title)
  // Comparison + version-pinned content is what goes wrong fastest; weight accordingly.
  const score =
    months * 1.0 +
    Math.min(versionHits, 12) * 0.8 +
    (datedTitle ? 2 : 0) +
    (comparison ? 3 : 0)
  const reasons = []
  if (comparison) reasons.push('comparison')
  if (datedTitle) reasons.push('year-pinned')
  if (versionHits) reasons.push(`${versionHits} version refs`)
  reasons.push(`${months.toFixed(1)}mo old`)
  return { score, ageDays, versionHits, datedTitle, comparison, reasons }
}

const traffic = existsSync(TRAFFIC) ? JSON.parse(readFileSync(TRAFFIC, 'utf8')) : { pages: [] }
const visitorsByPath = new Map(
  (traffic.pages || []).map((p) => [p.path.replace(/\/$/, ''), p.visitors])
)

const rows = []
for (const file of readdirSync(BLOG_DIR)) {
  if (!file.endsWith('.mdx')) continue
  const slug = file.replace(/\.mdx$/, '')
  const raw = readFileSync(join(BLOG_DIR, file), 'utf8')
  const { fm, body } = frontmatter(raw)
  const path = `/blog/${slug}`
  const visitors = visitorsByPath.get(path) || 0
  if (!visitors && !includeAll) continue
  const s = staleness(fm, body, slug)
  // No-traffic posts (in --all mode) still get a baseline so pure-staleness surfaces.
  const priority = (visitors || 0.5) * s.score
  rows.push({ slug, path, visitors, date: fm.date || '?', ...s, priority })
}

rows.sort((a, b) => b.priority - a.priority)

const flag = (r) =>
  r.priority > 200 ? '🔴' : r.priority > 60 ? '🟠' : r.priority > 20 ? '🟡' : '🟢'

const table = [
  '| # | Flag | Post | Visitors | Age | Priority | Why |',
  '|---|------|------|----------|-----|----------|-----|',
  ...rows.map(
    (r, i) =>
      `| ${i + 1} | ${flag(r)} | \`${r.slug}\` | ${r.visitors || '—'} | ${(r.ageDays / 30).toFixed(1)}mo | ${r.priority.toFixed(0)} | ${r.reasons.join(', ')} |`
  ),
].join('\n')

const md = `# Content Refresh Queue

_Generated by \`scripts/content-refresh-audit.mjs\` from \`traffic-latest.json\` (${traffic.capturedFor || 'n/a'}). priority = visitors × staleness. Re-run after each weekly traffic paste._

**Read it as:** 🔴 fix this week · 🟠 fix this month · 🟡 verify next cycle · 🟢 evergreen, light touch.

${table}

> Staleness rises with: model-version references (Gen-4 / Veo 3.1 / GPT-5…), year-pinned titles (2026), comparison framing (vs / which / best / landscape), and raw age. A post that is high-traffic **and** version-pinned **and** old is where a wrong fact does the most damage.
`

console.log(md)
if (doWrite) {
  writeFileSync(OUT, md)
  console.log(`\n[refresh-audit] wrote ${OUT}`)
}
