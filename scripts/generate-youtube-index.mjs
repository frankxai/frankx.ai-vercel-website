import fs from 'fs'
import path from 'path'
import matter from 'gray-matter'

const CONTENT_DIR = path.join(process.cwd(), 'content/blog')
const OUTPUT_FILE = path.join(process.cwd(), 'data/youtube-index.ts')

/**
 * Scans MDX files for YouTube links and embeds to build a searchable index.
 */
function generateYoutubeIndex() {
  console.log('🔍 Indexing YouTube content...')
  
  const files = fs.readdirSync(CONTENT_DIR).filter(f => f.endsWith('.mdx'))
  const index = []

  files.forEach(file => {
    const filePath = path.join(CONTENT_DIR, file)
    const content = fs.readFileSync(filePath, 'utf8')
    const { data, content: body } = matter(content)
    
    // Match <YouTubeEmbed id="..." /> or youtube.com/watch?v=... or youtu.be/...
    const embedMatches = body.match(/<YouTubeEmbed[^>]*id=["']([^"']+)["'][^>]*>/g) || []
    const rawMatches = body.match(/(?:youtube\.com\/watch\?v=|youtu\.be\/)([a-zA-Z0-9_-]{11})/g) || []
    
    const ids = new Set([
      ...embedMatches.map(m => m.match(/id=["']([^"']+)["']/)[1]),
      ...rawMatches.map(m => m.split('/').pop().split('v=').pop())
    ])

    if (ids.size > 0) {
      index.push({
        slug: file.replace('.mdx', ''),
        title: data.title,
        videoIds: Array.from(ids),
        category: data.category || 'Uncategorized'
      })
    }
  })

  // Preserve manually maintained section (youtubeChannels) above the auto-generated marker
  let manualSection = ''
  if (fs.existsSync(OUTPUT_FILE)) {
    const existing = fs.readFileSync(OUTPUT_FILE, 'utf8')
    const marker = '// Auto-generated by scripts/generate-youtube-index.mjs'
    const markerIndex = existing.indexOf(marker)
    if (markerIndex > 0) {
      manualSection = existing.substring(0, markerIndex)
    }
  }

  const autoGenerated = `// Auto-generated by scripts/generate-youtube-index.mjs
// Last updated: ${new Date().toISOString()}

export interface YoutubeIndexEntry {
  slug: string
  title: string
  videoIds: string[]
  category: string
}

export const youtubeIndex: YoutubeIndexEntry[] = ${JSON.stringify(index, null, 2)};
`

  fs.writeFileSync(OUTPUT_FILE, manualSection + autoGenerated)
  console.log(`✅ Indexed ${index.length} articles with YouTube content.`)

  // Generate video-cross-refs.json for cross-linking
  generateCrossRefs(index)
}

/**
 * Generates bidirectional cross-references between videos and blog posts.
 * Also maps videos to watchlists if video-watchlists.json exists.
 */
function generateCrossRefs(index) {
  const CROSS_REF_FILE = path.join(process.cwd(), 'data/video-cross-refs.json')
  const WATCHLIST_FILE = path.join(process.cwd(), 'data/video-watchlists.json')

  const videoToBlog = {}
  const blogToVideo = {}

  index.forEach(entry => {
    blogToVideo[entry.slug] = entry.videoIds
    entry.videoIds.forEach(vid => {
      if (!videoToBlog[vid]) videoToBlog[vid] = []
      if (!videoToBlog[vid].includes(entry.slug)) {
        videoToBlog[vid].push(entry.slug)
      }
    })
  })

  // Map videos to watchlists
  const videoToWatchlist = {}
  if (fs.existsSync(WATCHLIST_FILE)) {
    const watchlists = JSON.parse(fs.readFileSync(WATCHLIST_FILE, 'utf8'))
    watchlists.forEach(wl => {
      wl.videoIds.forEach(vid => {
        if (!videoToWatchlist[vid]) videoToWatchlist[vid] = []
        if (!videoToWatchlist[vid].includes(wl.id)) {
          videoToWatchlist[vid].push(wl.id)
        }
      })
    })
  }

  const crossRefs = { videoToBlog, blogToVideo, videoToWatchlist }
  fs.writeFileSync(CROSS_REF_FILE, JSON.stringify(crossRefs, null, 2))
  console.log(`✅ Generated cross-refs: ${Object.keys(videoToBlog).length} video→blog, ${Object.keys(blogToVideo).length} blog→video mappings.`)
}

generateYoutubeIndex()
