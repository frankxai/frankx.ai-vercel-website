export function getV0TemplatePath(entryOrId: V0TemplateEntry | string) {
  const id = typeof entryOrId === 'string' ? entryOrId : entryOrId.id
  return `/v/${id}`
}

export function getV0TemplateById(id: string) {
  return v0TemplateEntries.find((entry) => entry.id === id)
}

export function getRelatedV0Templates(entry: V0TemplateEntry, limit = 3) {
  return v0TemplateEntries
    .filter((candidate) => candidate.id !== entry.id)
    .map((candidate) => {
      const brandScore = candidate.brand === entry.brand ? 3 : 0
      const channelScore = candidate.channels.filter((channel) => entry.channels.includes(channel)).length
      const familyScore = candidate.family === entry.family ? 2 : 0
      return { candidate, score: brandScore + channelScore + familyScore }
    })
    .filter((item) => item.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map((item) => item.candidate)
}
